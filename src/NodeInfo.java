
public class NodeInfo {
    public int nodeID;
    public int value;
    public int coordsX;
    public int coordsY;

    public NodeInfo(int nodeID, int value, int coordsX, int coordsY) {
        this.nodeID = nodeID;
        this.value = value;
        this.coordsX = coordsX;
        this.coordsY = coordsY;
    }
}
